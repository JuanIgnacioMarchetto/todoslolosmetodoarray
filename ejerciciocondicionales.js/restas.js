var num1 = prompt("ingrese num1");
var num2 = prompt("ingrese num2");

var resta =num1-num2;

if(resta>0){
    //par o impar
    document.write("<h1>es mayor a 0</h1>");
    let imparpar= resta %2;
    if(imparpar==0){
        document.write("<h1>es par</h1>");
    }else{
        document.write("<h1>es impar</h1>");
    }
}else{
    document.write("<h1>es menor o igual a 0</h1>");
}










































































































































































































